#include "libft.h"

int	ft_islower(int c)
{
	return ((c >= 'a' && c <= 'z') * 512);
}

int	ft_isupper(int c)
{
	return ((c >= 'A' && c <= 'Z') * 256);
}

int	ft_isalpha(int c)
{
	return ((ft_islower(c) || ft_isupper(c)) * 1024);
}
